// printf
int _write( int xFile, char *pcPtr, int xLen )
{
    HAL_StatusTypeDef xStatus;
    switch ( xFile ) {
    case STDOUT_FILENO: /*stdout*/
		xStatus = HAL_UART_Transmit( &huart1, (uint8_t*)pcPtr, xLen, HAL_MAX_DELAY );
		if ( xStatus != HAL_OK ) {
			errno = EIO;
			return -1;
		}
        break;
    case STDERR_FILENO: /* stderr */
		xStatus = HAL_UART_Transmit( &huart1, (uint8_t*)pcPtr, xLen, HAL_MAX_DELAY );
		if ( xStatus != HAL_OK ) {
			errno = EIO;
			return -1;
		}
        break;
    default:
        errno = EBADF;
        return -1;
    }
    return xLen;
}


